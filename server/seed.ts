export function createSeedDocument(): ScoreboardDocument {
  const teams: Team[] = teamData.map(([teamId, fullName, shortName, country, logoFile], index) => ({
    teamId,
    fullName,
    shortName,
    country,
    logoFile,
    isTheoryActive: true,
    isPracticeActive: true,
    isFinalist: false,
    displayOrder: index + 1,
    manualRankTheory: null,
    manualRankPractice: null,
    manualRankFinal: null,
  }));
  const participants = teams.flatMap((team) => participantNames[team.teamId].map((fullName, index) => ({
    participantId: `${team.teamId}-p${index + 1}`,
    teamId: team.teamId,
    fullName,
    isTheoryParticipant: true,
    isPracticeParticipant: true,
    isFinalParticipant: false,
    practiceSlot: index + 1,
    finalSlot: null,
  })));
  return {
    settings: {
      refreshIntervalMs: 2000,
      finalistsCount: 5,
      teamNameMode: 'full',
      finalInputMode: 'participant',
      transparentBackground: false,
    },
    teams,
    participants,
    theoryScores: participants.map((participant) => ({ participantId: participant.participantId, score: 0 })),
    practiceScores: participants.map((participant) => ({ participantId: participant.participantId, ...emptyScoreBreakdown() })),
    participantFinalScores: participants.map((participant) => ({ participantId: participant.participantId, ...emptyScoreBreakdown() })),
    teamFinalScores: teams.map((team) => ({ teamId: team.teamId, leg1: 0, leg2: 0 })),
  };
}
